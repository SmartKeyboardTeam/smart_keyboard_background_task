class StrayIcon:
    def __init__(self):
        self.menu = (MenuItem('Open', self.__open_app, default=True),
                     MenuItem('Quit', self.__close))
        self.icon = pystray.Icon(
            "SmartKeyboard", image, "SmartKeyboard", self.menu)

    def __new__(self):
        if not hasattr(self, 'instance'):
            self.instance = super(StrayIcon, self).__new__(self)
        return self.instance

    def run(self):
        daemon = Thread(target=self.__run_stray_icon,
                        daemon=True, name='StrayIcon')
        daemon.start()

    def __open_app(self, icon, item):
        subprocess.Popen([f"{os.getcwd()}\\smart_keyboard_pc.exe"])

    def __close(self, icon, item):
        icon.stop()
        globals.is_running = False

    def __run_stray_icon(self):
        self.icon.run()
